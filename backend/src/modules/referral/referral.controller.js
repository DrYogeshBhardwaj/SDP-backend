const { PrismaClient } = require('@prisma/client');
const { successResponse, errorResponse } = require('../../utils/response');

const prisma = new PrismaClient();

exports.getDirectTeam = async (req, res) => {
    try {
        const userId = req.user.id;
        console.log(`[Referral API] Fetching team for User ID: ${userId} (${req.user.mobile})`);

        const referrals = await prisma.referral.findMany({
            where: {
                referrerId: userId
            },
            include: {
                referredUser: {
                    select: {
                        id: true,
                        mobile: true,
                        status: true,
                        createdAt: true,
                        pincode: true
                    }
                }
            },
            orderBy: {
                createdAt: 'desc'
            }
        });

        // Fetch bonuses generated by these referrals for the current user
        const bonusLedgers = await prisma.bonusLedger.findMany({
            where: {
                userId: userId,
                sourceUserId: { in: referrals.map(r => r.referredUser.id) }
            }
        });

        // Group bonuses by source user
        const bonusMap = {};
        for (const b of bonusLedgers) {
            if (b.sourceUserId) {
                bonusMap[b.sourceUserId] = (bonusMap[b.sourceUserId] || 0) + b.amount;
            }
        }

        // Format for frontend
        const formatTeam = (level) => referrals.filter(r => r.level === level).map(ref => ({
            mobile: ref.referredUser.mobile,
            createdAt: ref.referredUser.createdAt,
            status: ref.referredUser.status,
            pincode: ref.referredUser.pincode || 'N/A',
            amountGained: bonusMap[ref.referredUser.id] || 0,
            hasPurchasedPlan: (ref.referredUser.status === 'ACTIVE')
        }));

        const team = formatTeam(1);
        const teamLevel2 = formatTeam(2);

        return successResponse(res, 200, 'Team fetched successfully', { team, teamLevel2 });
    } catch (error) {
        console.error("Team fetch error:", error);
        return errorResponse(res, 500, error.message);
    }
};

exports.getNetworkData = async (req, res) => {
    try {
        const userId = req.user.id;

        const level1Count = await prisma.referral.count({
            where: { referrerId: userId, level: 1 }
        });

        const level2Count = await prisma.referral.count({
            where: { referrerId: userId, level: 2 }
        });

        return successResponse(res, 200, 'Network stats fetched', {
            level1Count,
            level2Count,
            directCount: level1Count,
            indirectCount: level2Count
        });
    } catch (error) {
        console.error("Network fetch error:", error);
        return errorResponse(res, 500, error.message);
    }
};

